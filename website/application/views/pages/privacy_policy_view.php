<h2>Cookies</h2>
<p>Cookies are pieces of text that may be provided to your computer through your web browser or application when you access a website or service. Your browser stores cookies in a manner associated with each website or service you visit. We use cookies to enable our servers to recognize your web browser or ElectionDesk application and tell us how and when you visit and use ElectionDesk through the web or through an application.</p>

<p>ElectionDesk cookies also allow us to track when you have accessed content through ElectionDesk. Each access is tracked using a unique identifier assigned to you in one or more cookies stored by your web browser or application and associated with ElectionDesk.</p>

<p>ElectionDesk cookies do not, by themselves, contain Personal Information, and we do not combine the general information collected through cookies with other Personal Information to tell us who you are. As noted, however, we do use cookies to identify that your web browser or application has accessed content through ElectionDesk and may associate that information with your Account if you have one.</p>

<p>This Privacy Policy covers our use of cookies only and does not cover the use of cookies by third parties. We do not control when or how third parties place cookies on your computer or device. For example, third party websites may set their own cookies.</p>

